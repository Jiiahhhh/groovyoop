package GENERIC.loop_condition;

public class soal1 {
    public static void main(String[] args) {
        // Loop dari 1-100
        for (int i = 0; i<=100; i++){
            // Cek jika bilangannya ganjil
            if (i % 2 == 0){
                System.out.println(i);
            }
        }
    }
}
